print("Conjunción: ")
print(f"{True} Y {True} es igual {True and True}")
print(f"{False} Y {True} es igual {False and True}")
print(f"{True} Y {False} es igual {True and False}")
print(f"{False} Y {False} es igual {False and False}")

print("Disyunción: ")
print(f"{True} o {True} es igual {True or True}")
print(f"{False} o {True} es igual {False or True}")
print(f"{True} o {False} es igual {True or False}")
print(f"{False} o {False} es igual {False or False}")

print("Negación: ")

print(f"No {True} es igual a {not True}")
print(f"No {False} es igual a {not False}")

